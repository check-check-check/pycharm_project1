# bool：根据传入的参数的逻辑值创建一个新的布尔值
print(bool()) #未传入参数

# 传入布尔类型时，按原值返回
print(bool(False))
print(bool(True))
# 传入字符串时，空字符串返回False，否则返回True
print(bool(''))
print(bool('aa'))
# 传入数值时，0值返回False，否则返回True
print(bool(0))
print(bool(2))
# 传入元组、列表、字典等对象时，元素个数为空返回False，否则返回True
